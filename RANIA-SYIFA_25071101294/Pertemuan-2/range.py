#range itu adalah batas/ jarak

#range(start, stop, step)

'''
Panggil fungsi range() dengan satu argumen.
'''
x = range(10)

'''
Panggil fungsi range() dengan dua argumen.
'''
x = range(3, 10)

'''
Panggil fungsi range() dengan tiga argumen.
'''
x = range(3, 10, 2)

#Menggunakan rentang
'''
Lakukan iterasi pada setiap nilai dalam suatu rentang:
'''
for i in range(10):
  print(i)

#Menggunakan Daftar untuk Menampilkan Rentang
'''
Konversikan rentang yang berbeda ke dalam daftar:
'''
print(list(range(5)))
print(list(range(1, 6)))
print(list(range(5, 20, 3)))
